package org.hitham.HITHAM.model;

public class SongModel {
 private String song_name;
 private String song_url;
 private String song_raaga;
 private String song_taal;
 private String song_singer;
 private String song_composer;
public String getSong_name() {
	return song_name;
}
public void setSong_name(String song_name) {
	this.song_name = song_name;
}
public String getSong_url() {
	return song_url;
}
public void setSong_url(String song_url) {
	this.song_url = song_url;
}
public String getSong_raaga() {
	return song_raaga;
}
public void setSong_raaga(String song_raaga) {
	this.song_raaga = song_raaga;
}
public String getSong_taal() {
	return song_taal;
}
public void setSong_taal(String song_taal) {
	this.song_taal = song_taal;
}
public String getSong_singer() {
	return song_singer;
}
public void setSong_singer(String song_singer) {
	this.song_singer = song_singer;
}
public String getSong_composer() {
	return song_composer;
}
public void setSong_composer(String song_composer) {
	this.song_composer = song_composer;
}

	 
 
}
