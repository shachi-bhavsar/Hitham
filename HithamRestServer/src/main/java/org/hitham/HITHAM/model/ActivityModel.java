package org.hitham.HITHAM.model;

public class ActivityModel {
	private String student_pk,recording_id,student_activity_type,student_activity_time;

	public String getStudent_pk() {
		return student_pk;
	}

	public void setStudent_pk(String student_pk) {
		this.student_pk = student_pk;
	}

	public String getRecording_id() {
		return recording_id;
	}

	public void setRecording_id(String recording_id) {
		this.recording_id = recording_id;
	}

	public String getStudent_activity_type() {
		return student_activity_type;
	}

	public void setStudent_activity_type(String student_activity_type) {
		this.student_activity_type = student_activity_type;
	}

	public String getStudent_activity_time() {
		return student_activity_time;
	}

	public void setStudent_activity_time(String student_activity_time) {
		this.student_activity_time = student_activity_time;
	}
}
